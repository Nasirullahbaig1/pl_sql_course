CREATE OR REPLACE TRIGGER DEL_TRG
BEFORE DELETE ON MAIN_TABLE
FOR EACH ROW
BEGIN
    INSERT INTO BACKUP_TABLE VALUES(:OLD.EMP_ID, :OLD.EMP_NAME, :OLD.EMP_SALARY);
END;
