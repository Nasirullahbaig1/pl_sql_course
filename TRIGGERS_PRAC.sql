    EMP_ID NUMBER(2),
    EMP_NAME VARCHAR2(15),
    EMP_SALARY VARCHAR2 (6)
    );
    
CREATE TABLE BACKUP_TABLE (
    ID NUMBER(2),
    NAME VARCHAR2(15),
